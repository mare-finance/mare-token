const func: DeployFunction = async (hre: HardhatRuntimeEnvironment) => {
    const {
        deployments: { deploy, get },
        getNamedAccounts,
    } = hre;

    if (recipients.length !== amounts.length)
        throw new Error("Recipients and amounts length mismatch");
    const totalAmount = amounts.reduce(
        (a, b) => a.add(b),
        ethers.BigNumber.from(0)
    );
    if (totalAmount.gt(vestingAmount))
        throw new Error("Total amount cannot be greator than vesting amount");
    if (totalAmount.lt(vestingAmount.sub(ethers.utils.parseUnits("1", 18))))
        throw new Error(
            "Total amount cannot be less than vesting amount by diff of 1e9"
        );

    const { adminAccount } = await getNamedAccounts();

    const mareDeploy = await get("Mare");
    const mare = await ethers.getContractAt(
        "contracts/interfaces/IERC20.sol:IERC20",
        mareDeploy.address
    );

    // Deploy Vester Cliff
    const teamVesterDeploy = await deploy("TeamVester", {
        from: adminAccount,
        log: true,
        args: [
            mare.address,
            adminAccount,
            vestingAmount,
            vestingBegin,
            vestingEnd,
            vestingCliff,
        ],
        contract: "contracts/VesterCliff.sol:VesterCliff",
    });
    const vesterCliff = await ethers.getContractAt(
        "VesterCliff",
        teamVesterDeploy.address
    );

    // Deploy Distributor
    const teamDistributorDeploy = await deploy("TeamDistributor", {
        from: adminAccount,
        log: true,
        args: [mare.address, vesterCliff.address, adminAccount],
        contract: "contracts/OwnedDistributor.sol:OwnedDistributor",
    });
    const ownedDistributor = await ethers.getContractAt(
        "OwnedDistributor",
        teamDistributorDeploy.address
    );

    // Set vester recipient to Distributor
    const vesterRecipient = await vesterCliff.recipient();
    if (vesterRecipient !== ownedDistributor.address) {
        await (
            await vesterCliff.setRecipient(ownedDistributor.address)
        ).wait(1);
    }

    // Edit Recipients
    await (await ownedDistributor.editRecipients(recipients, amounts)).wait(1);
};

